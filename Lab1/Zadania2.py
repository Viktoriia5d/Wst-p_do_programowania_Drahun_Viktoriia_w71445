#Zadania 2

print(int(3.0))
# int(3.0) zwraca 3, ponieważ int() odcina część dziesiętną

print(float(3))
# float(3) zwraca 3.0, ponieważ float() dodaje część dziesiętną .0 do liczby całkowitej

print(float("3"))
# float("3") zwraca 3.0, ponieważ konwertuje tekst "3" na liczbę zmiennoprzecinkową

print(str(12.4))
# str(12.4) zwraca "12.4", ponieważ str() konwertuje liczbę na tekst

print(bool(0))
# bool(0) zwraca False, ponieważ w typie logicznym liczba 0 jest interpretowana jako False