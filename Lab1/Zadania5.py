#Pobieranie długości boków
bok1=float(input("Podaj długość pierwszego boku "))
bok2=float(input("Podaj długość drugiego boku "))
print("Pole prostokąta wynosi:",bok1 * bok2)

#Obliczanie pola i obwodu
pole = bok1 * bok2
obwod = 2*(bok1+bok2)

# Wyświetlanie wyników
print(f"Pole wynosi: {pole}")
print(f"Obwód wynosi: {obwod}")