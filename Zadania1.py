#Zadania 1

print(1+2)
#dodawanie liczb całkowitych, wynik jest liczbą całkowitą
print(1+4.5)
#dodawanie liczb całkowitych i zmiennoprzecinkowych, wynik jest liczbą zmiennoprzecinkową
print(3/2)
#dzielenie, nawet między liczbami całkowitymi, daje liczbę zmiennoprzecinkową
print(4/2)
#dzielenie zawsze zwraca liczbę zmiennoprzecinkową, nawet jeśli jest dokładne
print(3//2)
#dzielenie zmiennoprzecinkowe, zwraca największą liczbę całkowitą
print(-3//2)
#dzielenie zmiennoprzecinkowe zaokrągla również w dół dla liczb ujemnych
print(11%2)
#moduł, reszta z dzielenia
print(2**10)
#potęgowanie, wynik jest liczbą całkowitą
print(8**(1/3))
#potęgowanie z wykładnikiem ułamkowym daje float